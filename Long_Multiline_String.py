# Python Program to Create a Long Multiline String.

# Method 1: Using Triple Quotations.
print("""Hello Everyone
This is my Demo File
Hope You All Will Like It""")


# Method 2: Using \n.
print("\nHello Everyone\nThis is my Demo File\nHope You All Will Like It")


# Method 3: Using \.
sagar = "\nHello Everyone\n"\
        "This is my Demo File\n"\
        "Hope You All Will Like It"

print(sagar)
